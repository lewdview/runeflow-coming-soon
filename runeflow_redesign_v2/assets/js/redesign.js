// JavaScript for Redesigned Coming Soon Page with Enhanced Animations

document.addEventListener('DOMContentLoaded', function() {
    const floatingRunes = document.querySelectorAll('.rune-float');
    const runeCards = document.querySelectorAll('.rune-card');
    const sections = document.querySelectorAll('section');

    // Function to randomly position and animate floating runes
    function randomizeFloatingRunes() {
        floatingRunes.forEach(floatingRune => {
            floatingRune.style.left = `${Math.random() * 100}%`;
            floatingRune.style.top = `${Math.random() * 100}%`;
            floatingRune.style.transform = `scale(${Math.random() * 0.7 + 0.3})`;
            floatingRune.style.animationDelay = `${Math.random() * 10}s`;
        });
    }

    // Initial positioning and animation
    randomizeFloatingRunes();

    // Add scroll fade-in animation to sections
    function animateSections() {
        const triggerBottom = window.innerHeight / 1.1;

        sections.forEach(section => {
            const sectionTop = section.getBoundingClientRect().top;

            if(sectionTop < triggerBottom) {
                section.classList.add('show');
            } else {
                section.classList.remove('show');
            }
        });
    }

    window.addEventListener('scroll', animateSections);
    animateSections(); // Initial check

    // Handle rune selection animation
    window.selectRune = function(runeId) {
        runeCards.forEach(card => {
            card.classList.remove('selected');
            card.querySelector('.select-btn').textContent = 'Select This Rune';
        });
        const selectedCard = document.querySelector(`.rune-card[data-rune="${runeId}"]`);
        if (selectedCard) {
            selectedCard.classList.add('selected');
            selectedCard.querySelector('.select-btn').textContent = 'Selected!';
        }
    };

    // Simulate opening a payment modal with smooth fade-in
    window.openPayment = function(method) {
        const modal = document.getElementById('successModal');
        modal.style.display = 'flex';
        modal.classList.add('show');
        setTimeout(() => {
            modal.classList.remove('show');
        }, 3000); // Auto hide after 3 seconds
    };

    // Close modal
    window.closeModal = function() {
        const modal = document.getElementById('successModal');
        modal.style.display = 'none';
        modal.classList.remove('show');
    };
});


// CSS Adjustments for Animations
const style = document.createElement('style');
style.innerHTML = `
    section {
        opacity: 0;
        transform: translateY(50px);
        transition: opacity 0.6s ease, transform 0.6s ease;
    }

    section.show {
        opacity: 1;
        transform: translateY(0);
    }

    .modal.show {
        opacity: 1;
        transform: scale(1);
    }
`;
document.head.appendChild(style);





































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































